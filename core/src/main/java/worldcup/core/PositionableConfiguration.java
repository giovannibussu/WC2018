package worldcup.core;

public class PositionableConfiguration {

	
	private int passaggioTurno;
	private int posizioneTabellone;
	private int risultato;
	private int risultatoEsatto;
	
	public int getPassaggioTurno() {
		return passaggioTurno;
	}
	public void setPassaggioTurno(int passaggioTurno) {
		this.passaggioTurno = passaggioTurno;
	}
	public int getPosizioneTabellone() {
		return posizioneTabellone;
	}
	public void setPosizioneTabellone(int posizioneTabellone) {
		this.posizioneTabellone = posizioneTabellone;
	}
	public int getRisultato() {
		return risultato;
	}
	public void setRisultato(int risultato) {
		this.risultato = risultato;
	}
	public int getRisultatoEsatto() {
		return risultatoEsatto;
	}
	public void setRisultatoEsatto(int risultatoEsatto) {
		this.risultatoEsatto = risultatoEsatto;
	}
}
